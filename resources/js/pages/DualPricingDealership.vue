<script setup>
import { ref, onMounted } from 'vue';
import MainLayout from './MainLayout.vue';

const expandedDetail = ref(null);
const showImageModal = ref(false);
const currentImage = ref('');
const currentImageAlt = ref('');

const toggleDetail = (detail) => {
  if (expandedDetail.value === detail) {
    expandedDetail.value = null;
  } else {
    expandedDetail.value = detail;
  }
};

const openImageModal = (image, alt) => {
  currentImage.value = image;
  currentImageAlt.value = alt;
  showImageModal.value = true;
};

const closeImageModal = () => {
  showImageModal.value = false;
};

onMounted(() => {
  // Initialize animation observer for scroll reveal
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('animate');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1 });

  // Observe all elements with scroll-reveal class
  document.querySelectorAll('.scroll-reveal').forEach(element => {
    observer.observe(element);
  });
  
  // Add slide-in animations for hero section
  const slideElements = document.querySelectorAll('.slide-in-left, .slide-in-right');
  slideElements.forEach(element => {
    element.classList.add('animate');
  });
});
</script>

